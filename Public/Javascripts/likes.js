  

var inc1 = (function() {

        var first = true;
        return function() {
            first ? liked() : unliked();
            first = !first;
        }
                })();

    function liked() {
            var thisrating = document.getElementById('rating1');
            var number = thisrating.innerHTML;
            number++;
            thisrating.innerHTML = number;
            document.getElementById("rating1").style.color = "#e95787";
            document.getElementById("heart1").style.color = "#e95787";
    };

    function unliked(){

        var thisrating = document.getElementById('rating1');
            var number = thisrating.innerHTML;
            number--;
            thisrating.innerHTML = number;
            document.getElementById("rating1").style.color = "#abafc4";
            document.getElementById("heart1").style.color = "#abafc4";

    };


